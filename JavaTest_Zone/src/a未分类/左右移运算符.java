package a未分类;

public class 左右移运算符 {

	public static void main(String[] args) {
		System.out.println(3<<1);//向左移动  *2N次方
		System.out.println(3<<2);
		System.out.println(3<<3);
		
		System.out.println(4>>1);//向右移动  /2N次方  会保持 int类型
		System.out.println(4>>2);
		System.out.println(5>>2);
		
	}
}
